// arrow function
function plusSignFun(){
    if(count.value >= 0){
        minesSign.style.cssText = `display: flex;`;
    }
    count.value++;
}

function minesSignFun(){
    if(count.value == 1){
        minesSign.style.cssText = `display: none`;
    }
    count.value--;
}


// Object with array (local storge)

let Order = [];

if(localStorage.order != null){
    Order = JSON.parse(localStorage.order);
}
else{
    Order = [];
}
var OrderData = {};

// add to cart btn

function addToCartbtn(){
    OrderData = {
        itemTitle: itemTitle.innerHTML,
        price: price.innerHTML,
        count: count.value,
    }
    Order.push(OrderData);
    localStorage.setItem('order', JSON.stringify(Order))
    emptyCart.style.cssText = `display: none;`;
    cartWithOrders.style.cssText = `display: flex;`;
    showData();
}

// show data
var cartCounter = document.getElementById('cartCounter');
var addedOrders = document.getElementById('addedOrders');
var total = document.getElementById('total');
var addedOrders2 = document.getElementById('addedOrders2');
var total2 = document.getElementById('total2');
var cartCounter2 = document.getElementById('cartCounter2');

function showData(){
    let newElement = '';
    let TPSumer = 0;
    let counterSumer = 0;
    for(let i = 0; i < Order.length; i++){
        newElement += `
            <div class="newElement">

                <div class="newE-l-s">

                    <div class="addedTitle">${Order[i].itemTitle}</div>

                        <div class="newE-inf">

                            <div class="addedCount">${Order[i].count}</div>
                            <div class="addedPrice">${Order[i].price}</div>
                            <div class="addedTotalPrice">${(Order[i].price * Order[i].count).toFixed(2)}</div>

                        </div>

                    </div>

                <div class="newE-r-s" onclick="deleteOneItem(${i});">X</div>

            </div>
        `
        TPSumer = +TPSumer + +(Order[i].price * Order[i].count).toFixed(2);
        counterSumer = +counterSumer + +Order[i].count;
    }
    addedOrders.innerHTML = newElement;
    total.innerHTML = TPSumer;
    addedOrders2.innerHTML = newElement;
    total2.innerHTML = TPSumer;
    cartCounter.innerHTML = `&nbsp( ${counterSumer} )`;
    tmpCountSumer = counterSumer;
    cartCounter2.innerHTML = counterSumer;
}
showData();

// delete one item

function deleteOneItem(i){
    Order.splice(i,1);
    localStorage.order = JSON.stringify(Order);
    showData();
    if(Order.length > 0){
        emptyCart.style.cssText = `display: none;`;
        cartWithOrders.style.cssText = `display: flex;`;
    }
    else{
        emptyCart.style.cssText = `display: flex;`;
        cartWithOrders.style.cssText = `display: none;`;
    }
}

// delete all items
var deleteAllBtn = document.getElementById('deleteAll');

function DeleteAll(){
    Order.splice(0,Order.length);
    localStorage.order = JSON.stringify(Order);
    if(Order.length > 0){
        emptyCart.style.cssText = `display: none;`;
        cartWithOrders.style.cssText = `display: flex;`;
    }
    else{
        emptyCart.style.cssText = `display: flex;`;
        cartWithOrders.style.cssText = `display: none;`;
    }
    showData();
}

